// 设计模式分类
// 1、创建型设计模式    专注于处理对象创建机制
// 包括：Constructor（构造器）、Factory（工厂）、Abstract（抽象）、Prototype（原型）、Singleton（单例）和Builder（生成器）
// 2、结构型设计模式
// 结构型模式与对象组合有关，通常可以找出在不同对象之间建立关系的简单方法
// 包括：Decorator（装饰者）、Facade（外观）、Flyweight（享元）、Adapter（适配器）和Proxy（代理）
// 3、行为设计模式
// 行为模式专注于改善或简化系统中不同对象之间的通信
// 包括：Iterator（迭代器）、Mediator（中介者）、Observer（观察者）和Visitor（访问者）